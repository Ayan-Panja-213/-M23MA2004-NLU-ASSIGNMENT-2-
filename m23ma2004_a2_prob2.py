class BiLstm(nn.Module):
    def __init__(self, vocab_size):
        super().__init__()

        # Initialization
        self.embed = nn.Embedding(vocab_size, 64)
        self.lstm = nn.LSTM(64, 48, batch_first=True, bidirectional=True)
        self.dropout = nn.Dropout(0.2)
        self.fc = nn.Linear(96, vocab_size)

       #  Forward pass
    def forward(self, x):
        x = self.embed(x)  # embedding
        out, _ = self.lstm(x)    # BiLSTM Processing
        out = self.dropout(out)   # regularization
        return self.fc(out)

