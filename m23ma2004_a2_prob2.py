    loss_fn = nn.CrossEntropyLoss(label_smoothing=0.1)

    train_losses = []
    val_losses = []

    best_val = float("inf")

    for epoch in range(epochs):
        #  TRAINING
        model.train()
        total_loss = 0

        for a, b in zip(train_A, train_B):
            a = torch.tensor(a).unsqueeze(0)
            b = torch.tensor(b)

            pred = model(a)
            pred = pred.squeeze(0).view(-1, pred.shape[-1])
            b = b.view(-1)

            loss = loss_fn(pred, b)

            optimizer.zero_grad()
            loss.backward()

            # gradient clipping
            torch.nn.utils.clip_grad_norm_(model.parameters(), 3)

            optimizer.step()
            total_loss += loss.item()

        avg_train = total_loss / len(train_A)

        # VALIDATION
