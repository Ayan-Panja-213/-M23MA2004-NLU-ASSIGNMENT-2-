class RNN_B_Attn(nn.Module):
   def __init__(self,vocab_size):
    super().__init__()

    # Initialization
    self.embed=nn.Embedding(vocab_size,32)
    self.rnn=nn.RNN(32,128,batch_first=True)
    self.attn=nn.Linear(128,1)
    self.dropout=nn.Dropout(0.1)
    self.fc=nn.Linear(128,vocab_size)

    # Forward Pass
   def forward(self,x):
     x=self.embed(x)
     out,_=self.rnn(x)

      # Attention Mechanism
     attn_score=self.attn(torch.tanh(out))
     attn_wgt=torch.softmax(self.attn(out),dim=1)
     attn_out=attn_wgt*out
     out=attn_out+out             # Residual Connection
     out=self.dropout(out)

     return self.fc(out)

