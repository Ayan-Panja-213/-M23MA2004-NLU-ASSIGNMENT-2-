def train_names(model, train_A, val_A, train_B, val_B, epochs=20):
    optimzr = torch.optim.Adam(model.parameters(), lr=0.001)
    loss_fn = nn.CrossEntropyLoss()

    train_losses = []
    val_losses = []

    # training loop
    for epoch in range(epochs):
        model.train()
        total_loss = 0

        for a, b in zip(train_A, train_B):
            a = torch.tensor(a).unsqueeze(0)
            b = torch.tensor(b)

            #Forward + Loss + Backprop

            pred = model(a)
            pred = pred.squeeze(0).view(-1, pred.shape[-1])
            b = b.view(-1)

            loss = loss_fn(pred, b)

            optimzr.zero_grad()
            loss.backward()


            torch.nn.utils.clip_grad_norm_(model.parameters(), 5)

            optimzr.step()
            total_loss += loss.item()

        avg_train_loss = total_loss / len(train_A)

        # Validation
        model.eval()
        val_loss = 0

        with torch.no_grad():
            for a, b in zip(val_A, val_B):
                a = torch.tensor(a).unsqueeze(0)
                b = torch.tensor(b)

                pred = model(a)
                pred = pred.squeeze(0).view(-1, pred.shape[-1])
                b = b.view(-1)

                 # Loss Tracking

                loss = loss_fn(pred, b)
                val_loss += loss.item()

        avg_val_loss = val_loss / len(val_A)

        train_losses.append(avg_train_loss)
        val_losses.append(avg_val_loss)

        print(f"Epoch {epoch+1}: Train Loss = {avg_train_loss:.4f}, Val Loss = {avg_val_loss:.4f}")

