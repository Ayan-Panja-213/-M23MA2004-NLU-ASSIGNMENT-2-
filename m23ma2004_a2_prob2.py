# Name Generation Function for Vanila RNN and RNN with Basic Attention
def gen_names(model, max_len=12, temp=0.9):
    model.eval()
    device = next(model.parameters()).device


    # Token Initialization

    nm = char2num["^"]
    name = [nm]

     # Sequential Generation Loop
    for _ in range(max_len):
        x = torch.tensor(name).unsqueeze(0).to(device)

        with torch.no_grad():
            out = model(x)

        out = out[0, -1]

        prob = torch.softmax(out / temp, dim=0)

        # avoid early stop
        if len(name) < 5:
            prob[char2num["~"]] *=0.3

        # repetition penalty
        if len(name) > 1:
            prob[name[-1]] *= 0.8

        # normalize again
        prob = prob / prob.sum()

        # top-k sampling
        k = 10
        topk_prob, topk_idx = torch.topk(prob, k)
        topk_prob = topk_prob / topk_prob.sum()

        nm = topk_idx[torch.multinomial(topk_prob, 1)].item()

        if num2char[nm] == "~":
            break

        name.append(nm)

    return "".join(num2char[i] for i in name if num2char[i] not in ["^", "~"])

# Name Gen Func for BILSTM
def gen_names_bilstm(model, max_len=12,temp=0.5):
    import torch

    model.eval()

    vowels = set("aeiou")

    def is_valid(name):
        # must contain at least one vowel
        if not any(c in vowels for c in name):
            return False

        # avoid 3+ consonants in a row
        cons = 0
        for c in name:
            if c not in vowels:
                cons += 1
                if cons >= 3:
                    return False
            else:
                cons = 0
        return True

    while True:  # keep generating until valid name
        nm = char2num["^"]
        name = [nm]

        for _ in range(max_len):
            x = torch.tensor(name).unsqueeze(0)

            with torch.no_grad():
                emb = model.embed(x)
                out, _ = model.lstm(emb)

                hidden = model.lstm.hidden_size

                #  forward-only (BiLSTM fix)
                forward = out[:, :, :hidden]
                backward = torch.zeros_like(forward)

                out = torch.cat([forward, backward], dim=2)
                out = model.fc(out)

            out = out[0, -1]

            #  STRONGER SAMPLING
            prob = torch.softmax(out / temp, dim=0)

            # avoid early stop
            if len(name) < 4:
                prob[char2num["~"]] = 0

            # repetition penalty (last 2 chars)
            for token in set(name[-2:]):
                prob[token] *= 0.3

            prob = prob / prob.sum()

            #  tighter top-k
            k = 2
            topk_prob, topk_idx = torch.topk(prob, k)
            topk_prob = topk_prob / topk_prob.sum()

            nm = topk_idx[torch.multinomial(topk_prob, 1)].item()

            if num2char[nm] == "~":
                break

            name.append(nm)

        # decode
        result = "".join(num2char[i] for i in name if num2char[i] not in ["^", "~"])

        #  filter bad names
        if len(result) >= 4 and is_valid(result):
            return result

