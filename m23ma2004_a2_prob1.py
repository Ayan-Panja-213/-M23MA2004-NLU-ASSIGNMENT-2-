def plot_pca_cbow(model, vocab, id2wrd, words):
    """
    Perform PCA on selected word embeddings and plot in 2D.
    """

    embeddings = model.in_emb.weight.detach().cpu().numpy()

    # Collect vectors for selected words
    word_vecs = []
    valid_words = []

    for w in words:
        if w in vocab:
            word_vecs.append(embeddings[vocab[w]])
            valid_words.append(w)

    word_vecs = np.array(word_vecs)

    # Apply PCA
    pca = PCA(n_components=2)
    reduced = pca.fit_transform(word_vecs)

    # Plot
    plt.figure(figsize=(8, 6))

    for i, word in enumerate(valid_words):
        x, y = reduced[i]
        plt.scatter(x, y)
        plt.text(x + 0.01, y + 0.01, word, fontsize=10)

    plt.title("PCA Visualization of CBOW Word Embeddings")
    plt.xlabel("PC1")
    plt.ylabel("PC2")
    plt.grid()

    plt.show()

