#  DATASET


class CBOWDataset(Dataset):
    """
    Creates (context, target) pairs for CBOW.
    """
    def __init__(self, corpus, window_size):
        self.data = []

        for i in range(window_size, len(corpus) - window_size):
            context = []

            for j in range(-window_size, window_size + 1):
                if j != 0:
                    context.append(corpus[i + j])

            target = corpus[i]
            self.data.append((context, target))

    def __len__(self):
        return len(self.data)

    def __getitem__(self, idx):
        x, y = self.data[idx]
        return torch.tensor(x, dtype=torch.long), torch.tensor(y, dtype=torch.long)

#  MODEL (CBOW + NEGATIVE SAMPLING)

class CBOW(nn.Module):
    def __init__(self, vocab_size, embed_dim):
        super().__init__()

        self.in_emb = nn.Embedding(vocab_size, embed_dim)
        self.out_emb = nn.Embedding(vocab_size, embed_dim)

    def forward(self, context, target, negatives):
        """
        context: (batch, context_size)
        target: (batch,)
        negatives: (batch, k)
        """

        # Context embedding
        context_emb = self.in_emb(context)          # (batch, context, dim)

        # Weighted mean (better than simple mean)
        weights = torch.softmax(torch.norm(context_emb, dim=2), dim=1)
        context_vec = torch.sum(context_emb * weights.unsqueeze(2), dim=1)

        # Target embedding
        target_emb = self.out_emb(target)           # (batch, dim)

        # Negative embeddings
        neg_emb = self.out_emb(negatives)           # (batch, k, dim)

        # Positive score
        pos_score = torch.sum(context_vec * target_emb, dim=1)

        # Negative score
        neg_score = torch.bmm(neg_emb, context_vec.unsqueeze(2)).squeeze(2)

        # Loss
        loss = -torch.log(torch.sigmoid(pos_score)) \
               - torch.sum(torch.log(torch.sigmoid(-neg_score)), dim=1)

        return loss.mean()

#  TRAINING FUNCTION


def train_CBOW(model, train_ld, val_ld, vocab_size, epochs, lr, k):

    optimizer = torch.optim.Adam(model.parameters(), lr=lr)
    scheduler = torch.optim.lr_scheduler.StepLR(optimizer, step_size=5, gamma=0.5)

    train_ls = []
    val_ls = []

    for epoch in range(epochs):

        # ===== TRAIN =====
        model.train()
        total_train_loss = 0

        for x, y in train_ld:

            # Generate negative samples
            neg_samples = [neg_sample(k) for _ in range(len(y))]
            neg_samples = torch.tensor(neg_samples, dtype=torch.long)

            optimizer.zero_grad()
            loss = model(x, y, neg_samples)

            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 5)

            optimizer.step()
            total_train_loss += loss.item()

        scheduler.step()

        avg_train_loss = total_train_loss / len(train_ld)
        train_ls.append(avg_train_loss)

        # ===== VALIDATION =====
        model.eval()
        total_val_loss = 0

        with torch.no_grad():
            for x, y in val_ld:

                neg_samples = [neg_sample(k) for _ in range(len(y))]
                neg_samples = torch.tensor(neg_samples, dtype=torch.long)

                loss = model(x, y, neg_samples)
                total_val_loss += loss.item()

        avg_val_loss = total_val_loss / len(val_ld)
        val_ls.append(avg_val_loss)

        print(f"Epoch {epoch+1} | Train: {avg_train_loss:.4f} | Val: {avg_val_loss:.4f}")

    return train_ls, val_ls

