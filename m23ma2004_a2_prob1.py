def most_similar(word, model, vocab, id2wrd, top_k=5):

    if word not in vocab:
        return []

    emb = model.in_emb.weight
    word_vec = emb[vocab[word]]

    sims = []

    for i in range(len(vocab)):
        sim = F.cosine_similarity(word_vec, emb[i], dim=0)
        sims.append((id2wrd[i], sim.item()))

    sims.sort(key=lambda x: x[1], reverse=True)

    return sims[1:top_k+1]

