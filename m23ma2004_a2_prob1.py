import torch.nn.functional as F

def analogy(a, b, c, model, vocab, id2wrd, top_k=5):
    """
    Performs analogy: a : b :: c : ?

    vector = v(b) - v(a) + v(c)
    """

    # Check if words exist
    for word in [a, b, c]:
        if word not in vocab:
            print(f"Word '{word}' not in vocabulary!")
            return []

    emb = model.in_emb.weight

    # Compute analogy vector
    vec = emb[vocab[b]] - emb[vocab[a]] + emb[vocab[c]]

    sims = []

    for i in range(len(vocab)):
        word = id2wrd[i]

        # skip input words
        if word in [a, b, c]:
            continue

        sim = F.cosine_similarity(vec, emb[i], dim=0)
        sims.append((word, sim.item()))

    # sort by similarity
    sims.sort(key=lambda x: x[1], reverse=True)

    return sims[:top_k]


